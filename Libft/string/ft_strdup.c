#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t		src_len;
	size_t		i;
	char		*dst;

	src_len = ft_strlen(s);
	dst = malloc(sizeof(char) * src_len + 1);
	i = 0;
	while (s[i])
	{
		dst[i] = s[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
